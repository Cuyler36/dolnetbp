u32 NETCFGCalcDataSum(u8* data) {
    u32 ret = 0;
    int i;

    for (i = 0; i < 256; i++) {
        ret += data[i];
    }

    return ret;
}

u8 NETCFGCalcSwapTableIndex(u8* data) {
    u32 value;

    value = NETCFGCalcDataSum(data);
    return value & 3;
}

void NETCFGEncode(u8* data) {
    u8 buf[256];
    u8 index = 0;
    int i;

    for (i = 0; i < 256; i++) {
        buf[i] = data[Table[i + (index * 256)]];
    }

    index = NETCFGCalcSwapTableIndex(buf);
    
    for (i = 0; i < 256; i++) {
        data[i] = buf[Table[i + (index * 256)]];
    }
}

void NETCFGDecode(u8* data) {
    u8 buf[256];
    u8 index;
    int i;
    
    index = NETCFGCalcSwapTableIndex(data);
    for (i = 0; i < 256; i++) {
        buf[Table[i + (index * 256)]] = data[i];
    }
    
    index = 0;
    for (i = 0; i < 256; i++) {
        data[Table[i + (index * 256)]] = buf[i];
    }
    
}

BOOL NETCFGCheckData(void* addr, u32 len, u8* md5Digest) {
    MD5Context context;
    u8 digest[16];
    BOOL result;

    MD5Init(&context);
    MD5Update(&context, (u8*)addr, len);
    MD5Final(digest, &context);

    if (memcmp(digest, md5Digest, sizeof(digest)) != 0) {
        result = FALSE;
    } else {
        result = TRUE;
    }

    return result;
}

BOOL NETCFGIsConfigFile(u8 attr, const CARDStat* stat) {
    u8 blank[4];

    memset(blank, 0, sizeof(blank));
    if (strcmp(stat->fileName, "__NETCONFIG.INF") == 0 &&
        memcmp(stat->gameName, blank, sizeof(blank)) == 0 &&
        memcmp(stat->company, blank, sizeof(stat->company)) == 0 &&
        stat->commentAddr == 0x2000 && stat->iconAddr == 0 && (attr & 0x20) != 0) {
        return TRUE;
    }
    return FALSE;
}
